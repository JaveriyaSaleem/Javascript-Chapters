// =======Loop of stars Star pattern ======
// increment
for (var i = 0; i<=10; i++){
    for(var j=0; j<=i; j++){
        document.write("*");
    }

document.write("<br>");

}
document.write("<br>");
// decrement 
for (var i = 10; i>=0; i--){
    for (var j=i; j>=0; j--){
        document.write("*");

    }
    document.write("<br>");
}

